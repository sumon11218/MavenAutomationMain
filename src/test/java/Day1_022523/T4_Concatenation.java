package Day1_022523;

public class T4_Concatenation {

    public static void main(String[] args) {

        String message, numberString;
        int number, number2;

        //declare string data type
        message = "Welcome to Automation class";
        numberString = "1";

        //declare int data type
        number = 1;
        number2 = 200;

        //I want to print string and int data type together in one print statement
        System.out.println("Result is " + message + " " + number + " " + number2);

    }//end of main method

}//end of java class
